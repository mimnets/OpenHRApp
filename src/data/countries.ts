// Flag emoji helper â€” converts ISO code to flag emoji via Unicode regional indicators
export const getFlagEmoji = (code: string): string => {
  return code
    .toUpperCase()
    .split('')
    .map(char => String.fromCodePoint(0x1F1E6 + char.charCodeAt(0) - 65))
    .join('');
};

